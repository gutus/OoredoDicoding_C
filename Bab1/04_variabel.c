/*Variabel */

#include <stdio.h>
int main()
{
    /* Integer*/
    int i = 5;
    /* Float */
    float riil = 5.05;
    /* String */
    char text = "Ini adalah sample text";

    /* Print nilai variabel */
    printf("Ini adalah sample print variabel integer>> %d.\n", i);
    printf("Ini adalah sample print variabel float %f. \n", riil);
    printf("Ini adalh sample print variabel string %d.\n", text);
    printf("\n\n");

    /* Contoh Soal*/
    int bebek = 10;
    float luas = 2.5;
    float keliling = 2 * 3.14 * 5.5;

    /* Eksekusi Program */
    printf("Bebekku ada %d. \n", bebek);
    printf("Luas kebunku %f hektar.\n", luas);
    printf("Keliling dari lingkaran yg jari-jarinya 5.5cm adalah %fcm.\n", keliling);
}